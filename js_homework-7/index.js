// Для заданной строки слов вернуть длину самого короткого слова

function findShort(s) {
  return s.split(" ").reduce((result, text) => {
    if (result.length > text.length) {
      return (result = text);
    } else {
      return result;
    }
  }).length;
}

// задача №3
// Проверить, одиноково ли количество символов "x" и"o" в строке.
// Метод должен возвращать логическое значение и не учитывать регистр.
// Строка может содержать любые символы

function XO(str) {
  let lowerStr = str.toLowerCase().split("");
  let x = lowerStr.reduce((num, let) => (let === "x" ? (num += 1) : num), 0);
  let o = lowerStr.reduce((num, let) => (let === "o" ? (num += 1) : num), 0);
  return o === x;
}

