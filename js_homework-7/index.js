// Дополните метод/функцию так, чтобы она преобразовывала слова,
// разделенные дефисом или подчеркиванием, в верблюжий регистр.
// Первое слово в выводе должно быть написано с заглавной буквы
// только в том случае, если исходное слово было написано
// с заглавной буквы (так называемый верхний верблюжий регистр,
// который также часто называют регистром Паскаля).
// Остальные слова должны быть написаны с заглавной буквы всегда.

function toCamelCase(str) {
  return str
    .split(/[-_]/)
    .map((element, index) =>
      index === 0 ? element : element[0].toUpperCase() + element.slice(1),
    )
    .join("");
}

// задача №8
//Переместите первую букву каждого слова в конец,
// затем добавьте к концу слова "ay".
// Знаки препинания не трогайте.

function pigIt(str) {
  return str
    .split(" ")
    .map(el => {
      if (/^[a-zA-Z]+$/.test(el)) {
        return el.slice(1) + el[0] + "ay";
      } else {
        return el;
      }
    })
    .join(" ");
}

// задача №9
// Доработайте решение так, чтобы оно разбивало строку на подстроки
//  по два символа в списке/массиве (в зависимости от используемого языка).
// Если в строке нечетное количество символов, недостающий второй символ
// последней пары следует заменить на подчеркивание ('_').

function solution(str) {
  const result = [];
  for (let i = 0; i < str.length; i += 2) {
    result.push(str[i] + (str[i + 1] || "_"));
  }
  return result;
}

// задача №10
// Напишите функцию, которая принимает строку и возвращает ту же строку,
// в которой все символы с четными индексами в каждом слове написаны
// заглавными буквами, а все символы с нечетными индексами — строчными.
// Нумерация начинается с нуля, поэтому нулевой индекс — четный,
// поэтому этот символ должен быть написан заглавными буквами,
// а для каждого слова нужно начинать с начала.

function toWeirdCase(string) {
  return string.split(" ").map(el => {
    return el
      .split("")
      .map((lit, indx) =>
        indx % 2 === 0 ? lit.toUpperCase() : lit.toLowerCase()
      ).join("")
  }).join(" ")
}

