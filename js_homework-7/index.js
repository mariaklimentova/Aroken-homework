// задача №1
// Написать функцию, которая может принимать в качестве аргумента любое неотрицательное
// целое число и возвращать его ввиде последовательности цифр в поряде убывания.
// По сути вам нужно переставить цфры так, чтобы получилось максимально возможное число

function descendingOrder(n) {
  const arr = String(n).split("").map(Number);
  let num = arr.sort((a, b) => b - a).join("");
  return Number(num);
}

