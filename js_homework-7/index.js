// Все пробелы в строке должны сохраняться.

function reverseWords(str) {
  return str
    .split(" ")
    .map(string => string.split("").reverse().join(""))
    .join(" ");
}

// задача №5
// Нарциссическое число (или число Армстронга) — это положительное число,
// которое представляет собой сумму его собственных цифр,
// каждая из которых возведена в степень, равную количеству цифр в заданном основании.
// В этой ката мы будем работать только с десятичной системой счисления (основание 10).

function narcissistic(value) {
  let arrValue = value.toString().split("");
  let sum = arrValue.reduce(
    (sum, num) => sum + Math.pow(Number(num), arrValue.length),
    0,
  );
  return sum === value;
}

// задача №6
// Напишите алгоритм, который принимает на вход массив и перемещает все нули в конец,
// сохраняя порядок остальных элементов.

function moveZeros(arr) {
  const nonZeros = arr.filter(element => element !== 0);
  const zeros = arr.filter(element => element === 0);
  return [...nonZeros, ...zeros];
}

